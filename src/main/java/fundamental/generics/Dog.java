package fundamental.generics;

public class Dog extends Animal{

    private String sound;
    private String eat;

    public Dog() {
    }

    public String getSound() {
        return sound;
    }

    public void setSound(String sound) {
        this.sound = sound;
    }

    public String getEat() {
        return eat;
    }

    public void setEat(String eat) {
        this.eat = eat;
    }
}
